<li>Предоставление ложной информации о персонажах, заходивших с одного компьютера.
<li>Прокачка на "ботах", т.е. специально заведенных персонажах.
<li>Использование сторонних программных средства для улучшения/автоматизации игровых возможностей (автокликеры и прочее).
<li>Прокачка на других игроках за кредиты или взаимная прокачка.
<li>Прокачка младшего уровня старшим.
<li>Финансовая прокачка (безвозмездная или сопоставимая с таковой передача Игроком своих игровых ценностей, покемонов, аккаунтов в постоянное или временное пользование другим Игрокам).
<li>Предложение в чате взаимной прокачки, заведомого проигрыша/выигрыша боя за кредиты.

<li> Использование в имени (нике) игрового персонажа или покемона ненормативной лексики, оскорблений, названий наркотических средств,
а также регистрация имён (ников), затрагивающих политику, разжигающих межнациональную рознь, являющихся ссылками на другие интернет-ресурсы,
являющихся схожими с именами (никами), используемыми Администрацией и Полицией игры.

<li>Использование ненормативной лексики и матозаменителей в чате/форуме/тренеркарте/кланкарте.
<li>Оскорбительные высказывания в адрес Администрации или Игроков, унижение их чести и достоинства, выраженное в неприличной форме.
<li>Сексуальные домогательства.
<li>Пропаганда расизма, националистические высказывания
<li>Дискриминация любого рода (по расовой, религиозной, половой принадлежности)
<li>Угрозы насилием и/или физической расправой в реальности.

<li>Флуд/спам в чате/форуме/тренеркарте/кланкарте
<li>Реклама сайтов, распространение ссылок на веб-сайты, содержание которых не относится к Лиге-17.


<li>Обман игроков (в т.ч. мошенничество при проведении торговых сделок, невыполнение условий сделки).
<li>Вымогательство/шантаж - принуждение к совершения сделки посредством угроз распространения сведений, позорящих потерпевшего, либо иных сведений, которые могут причинить существенный вред правам или законным интересам потерпевшего.
<li>ДОС-атаки, использование ошибок программного обеспечения.
<li>Взлом/похищение персонажей.
<li>Распространение ссылок на веб-сайты, содержание которых вызывает сбои в работе компьютера, а равно на веб-сайты, содержание которых направлено на незаконное получение паролей доступа к персонажам Pokezone.

<li>Покупки/продажи (равно как и попытки это сделать) у других Игроков персонажа/кредитов/покемонов/вещей за реальные деньги/вещи.
<li>Покупки/продажи (равно как и попытки это сделать) предметов/услуг, не имеющих отношения к Игре, за кредиты/вещи/покемонов Игры.
<li>Передача реквизитов своего персонажа(логин, пароль) другим игрокам, как безвозмездно, так и за игровые или реальные деньги.

<li>Попрошайничество.
<li>Черезмерное давление на Администрацию и Полицию Игры в чате и ЛС.
<li>Чрезмерное использование заглавных букв в сообщениях чата.
<li>Провокация других Игроков к нарушению данных правил.
