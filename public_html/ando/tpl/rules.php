<font size="4"><b>Правила Pokezone</b></font>

<p>После регистрации пользователь (в дальнейшем Игрок) получает в свое распоряжение игрового персонажа, стартового покемона, начальный набор предметов,
1000 кредитов (внутриигровая денежная единица) и доступ к миру Pokezone (в дальнейшем Игра).
<p>Для своего дальнейшего развития, игрок должен участвовать и побеждать в поединках с другими персонажами, ловить и развивать новых покемонов.
<p>Побеждая в поединках, Игрок получает бонусы в виде повышения своей репутации и уровня покемонов, а так же игровых денег и вещей.
<p>Кроме того, Игрок может получать игровые деньги и вещи, выполняя квесты и побеждая диких (не принадлежащих игрокам) покемонов.

